	PROGRAM FXIUE
C      Program to fix iue file for combination with FUSE file
      DIMENSION WAVE(10000),FLUX(10000)
      DATA IN1/5/
	DATA NOUT1/6/
	DATA ERRMN/7.0E-14/
	DATA ERRADD/3.0E-14/
	OPEN (UNIT=IN1, FILE='D:\CVSYSTEMS\RWSEX\CASE3A.DAT', 
     $STATUS='OLD')
	OPEN (UNIT=NOUT1, FILE='D:\CVSYSTEMS\RWSEX\CASE3AFX.DAT',
     $STATUS='REPLACE')
  100 FORMAT (6X,F7.2,E14.5)
  101 FORMAT (5X,F15.10,1PE23.13,1PE23.13)
      DO K=1,100
		CALL RANDOM(X)
	END DO
      DO I=1,10000
		READ (IN1,100,END=1) WL,FX
		WAVE(I)=WL
		FLUX(I)=FX
	END DO
    1 NCT=I-1
      DO J=1,NCT
		CALL RANDOM(X)
		ER=ERRMN-X*ERRADD
		WRITE (NOUT1,101) WAVE(J),FLUX(J),ER
	END DO
	CLOSE (UNIT=IN1, STATUS = 'KEEP')
	CLOSE (UNIT=NOUT1, STATUS = 'KEEP')
	STOP
	END PROGRAM FXIUE